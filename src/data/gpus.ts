// --- Helper Functions ---

export function getGPUBySlug(slug: string): GPU | undefined {
  return gpus.find((g) => g.slug === slug);
}

export function getGPUsByBrand(brand: Brand): GPU[] {
  return gpus.filter((g) => g.brand === brand).sort((a, b) => b.currentPrice - a.currentPrice);
}

export function getGPUsByTier(tier: Tier): GPU[] {
  return gpus.filter((g) => g.tier === tier).sort((a, b) => b.benchmarks.gaming4k - a.benchmarks.gaming4k);
}

export function getSimilarGPUs(gpu: GPU, limit = 4): GPU[] {
  const priceRange = gpu.currentPrice * 0.4;
  const sameTier = gpus.filter((g) => g.slug !== gpu.slug && g.tier === gpu.tier);
  const nearPrice = gpus.filter(
    (g) =>
      g.slug !== gpu.slug &&
      Math.abs(g.currentPrice - gpu.currentPrice) <= priceRange
  );

  // Prioritize: same tier + similar price, then same tier, then similar price
  const scored = gpus
    .filter((g) => g.slug !== gpu.slug)
    .map((g) => {
      const tierMatch = g.tier === gpu.tier ? 2 : 0;
      const priceDiff = Math.abs(g.currentPrice - gpu.currentPrice);
      const priceScore = Math.max(0, 1 - priceDiff / (gpu.currentPrice * 0.5));
      return { gpu: g, score: tierMatch + priceScore };
    })
    .sort((a, b) => b.score - a.score);

  return scored.slice(0, limit).map((s) => s.gpu);
}

