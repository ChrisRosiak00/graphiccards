export function getTopComparisons(): { gpu1: GPU; gpu2: GPU; slug: string }[] {
  const results: { gpu1: GPU; gpu2: GPU; slug: string }[] = [];
  for (const [slug1, slug2] of topComparisonPairs) {
    const gpu1 = getGPUBySlug(slug1);
    const gpu2 = getGPUBySlug(slug2);
    if (gpu1 && gpu2) {
      results.push({
        gpu1,
        gpu2,
        slug: `${slug1}-vs-${slug2}`,
      });
    }
  }
  return results;
}

export function getAllComparisonSlugs(): string[] {
  return getTopComparisons().map((c) => c.slug);
}

